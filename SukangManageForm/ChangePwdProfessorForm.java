    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String cp=CurPwd.getText();
        String np=NextPwd.getText();
        String ci=CurID.getText();
        String full;

        ChangePwdStudent cps=new ChangePwdStudent();
        cps.Find();
        cps.SplitLine(ci);

        if(ci.equals(cps.SPline0())&&cp.equals(cps.SPline1())){
            full=ci+" "+np+" "+cps.SPline2()+" "+cps.SPline3()+" "+cps.SPline4();

            
            try {
                cps.Delete();
            } catch (IOException ex) {
                Logger.getLogger(ChangePwdProfessorForm.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            try {
                cps.FindLine(ci);
            } catch (IOException ex) {
                Logger.getLogger(ChangePwdProfessorForm.class.getName()).log(Level.SEVERE, null, ex);
            }
           
            try {
                cps.Add(full);
            } catch (IOException ex) {
                Logger.getLogger(ChangePwdProfessorForm.class.getName()).log(Level.SEVERE, null, ex);
            }
             JOptionPane a=new JOptionPane();
            a.showMessageDialog(null, "비밀번호가 변경 되었습니다.");
        }
        else{
              JOptionPane a=new JOptionPane();
            a.showMessageDialog(null, "아이디,패스워드가 일치하지 않습니다.");
        }
            
    }//GEN-LAST:event_jButton2ActionPerformed

